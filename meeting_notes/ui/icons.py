def _svg(name: str, color: str, size: int) -> str:
    body = ICONS.get(name, "")
    if name in _FILLED:
        fill, stroke = color, "none"
    else:
        fill, stroke = "none", color
    return (
        f'<svg xmlns="http://www.w3.org/2000/svg" width="{size}" height="{size}" '
        f'viewBox="0 0 24 24" fill="{fill}" stroke="{stroke}" stroke-width="2" '
        f'stroke-linecap="round" stroke-linejoin="round">{body}</svg>'
    )


def _dpr() -> float:
    app = QApplication.instance()
    if app is not None:
        screen = app.primaryScreen()
        if screen is not None:
            return float(screen.devicePixelRatio())
    return 1.0


def pixmap(name: str, color: str, size: int = 20) -> QPixmap:
    dpr = _dpr()
    renderer = QSvgRenderer(QByteArray(_svg(name, color, size).encode("utf-8")))
    pm = QPixmap(int(size * dpr), int(size * dpr))
    pm.setDevicePixelRatio(dpr)
    pm.fill(Qt.GlobalColor.transparent)
    painter = QPainter(pm)
    renderer.render(painter)
    painter.end()
    return pm


def icon(name: str, color: str, size: int = 20) -> QIcon:
    return QIcon(pixmap(name, color, size))
